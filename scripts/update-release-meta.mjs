import { execSync } from 'node:child_process'
import { readFileSync, writeFileSync } from 'node:fs'
import { resolve } from 'node:path'

const root = process.cwd()
const pkg = JSON.parse(readFileSync(resolve(root, 'package.json'), 'utf8'))

const formatDate = (iso) =>
  new Date(iso).toLocaleDateString('en-GB', {
    day: '2-digit',
    month: 'short',
    year: 'numeric',
    timeZone: 'UTC',
  })

const raw = execSync('git log -n 12 --date=iso-strict --pretty=format:%h\\|%ad\\|%s', {
  cwd: root,
  encoding: 'utf8',
}).trim()

const commits = raw
  .split('\n')
  .filter(Boolean)
  .map((line, idx) => {
    const [commit, dateIso, ...rest] = line.split('|')
    return {
      commit,
      date: formatDate(dateIso),
      message: rest.join('|').trim(),
      current: idx === 0,
    }
  })

const latest = commits[0] ?? { commit: 'unknown', date: formatDate(new Date().toISOString()) }
const version = pkg.version && pkg.version !== '0.0.0' ? `v${pkg.version}` : 'v1.0.0'

const contents = `/* AUTO-GENERATED by scripts/update-release-meta.mjs. Do not edit manually. */
export type ReleaseCommit = {
  commit: string
  date: string
  message: string
  current: boolean
}

export const RELEASE_META = {
  version: '${version}',
  latestCommit: '${latest.commit}',
  lastPublished: '${latest.date}',
  repoLabel: 'viharnar / codex',
  repoUrl: 'https://github.com/vnar/reg-sdlc',
  authorName: 'Vihar Nar',
  authorUrl: 'https://www.linkedin.com/in/viharnar/',
} as const

export const RELEASE_COMMITS: ReleaseCommit[] = ${JSON.stringify(commits, null, 2)} as ReleaseCommit[]
`

writeFileSync(resolve(root, 'src/data/releaseMeta.ts'), contents, 'utf8')
console.log('Updated src/data/releaseMeta.ts')
